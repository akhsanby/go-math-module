package gomathmodule

func Plus(number1 float64, number2 float64) float64 {
	return number1 + number2
}

func Minus(number1 float64, number2 float64) float64 {
	return number1 - number2
}

func Multiplied(number1 float64, number2 float64) float64 {
	return number1 * number2
}

func Divided(number1 float64, number2 float64) float64 {
	return number1 / number2
}